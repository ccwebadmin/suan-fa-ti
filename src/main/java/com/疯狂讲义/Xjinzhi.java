public class Xjinzhi {
    public static void main(String[] args) {
        //二进制以0b或者0B开头
        int var1 = 0b1111;
        System.out.println(var1);//15
        //八进制 以0开头
        int var2 = 013;
        System.out.println(var2);//11
        //16进制  以0x或0X开头
        int var3 = 0Xa1;
        System.out.println(var3);//161
    }
}
